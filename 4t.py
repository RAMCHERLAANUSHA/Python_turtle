import turtle as t

screen = t.Screen()

screen.setup(height=700, width=700)

t.goto(0,0)
t.down()
t.fillcolor('blue')
t.begin_fill()
t.circle(40)
t.end_fill()
t.up()
t.home()

t.goto(200,200)
t.down()
t.fillcolor('green')
t.begin_fill()
t.circle(40)
t.end_fill()
t.up()
t.home()

t.goto(-200,200)
t.down()
t.fillcolor('orange')
t.begin_fill()
t.circle(40)
t.end_fill()
t.up()
t.home()

t.goto(200,-200)
t.down()
t.fillcolor('red')
t.begin_fill()
t.circle(40)
t.end_fill()
t.up()
t.home()

t.goto(-200,-200)
t.down()
t.fillcolor('brown')
t.begin_fill()
t.circle(40)
t.end_fill()
t.up()
t.home()

screen.update()
screen.mainloop()